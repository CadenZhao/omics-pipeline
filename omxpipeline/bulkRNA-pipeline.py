def main():
    START_TIME = datetime.now()
    args=parser()
    
    # -----------------------
    
    if args.command == 'tMode':
        # arguments
        (transcriptome_index, transcriptome_fasta, genome_fasta, annot, in_fq_dir, out_dir, type_of_lib, adaptor_dir, 
         thread, read_length, gffread, trimmmomatic, fastqc, multiqc, salmon, Rscript
        ) = (args.transcriptome_index, args.transcriptome_fasta, args.genome_fasta, args.annot, args.in_fq_dir, args.out_dir, args.type_of_lib, args.adaptor_dir,
             args.thread, args.read_length, args.gffread, args.trimmomatic, args.fastqc, args.multiqc, args.salmon, args.rscript
            )
        if type_of_lib == 'SE':
            sample_ids = [f.rstrip('.fastq.gz') for f in os.listdir(in_fq_dir) if f.endswith('.fastq.gz')]
        else:
            sample_ids= [f.rstrip('_1.fastq.gz') for f in os.listdir(in_fq_dir) if f.endswith('_1.fastq.gz')]
            
        # check if -x or -G at least one exist
        if transcriptome_fasta == None and genome_fasta == None and transcriptome_index == None:
            os.sys.exit('ERROR: -x or -X or -G must be set.  Please set one')

        # 0. build salmon index
        if transcriptome_index is not None:
            pass
        elif transcriptome_fasta is not None:
            transcriptome_index = build_salmon_index(salmon, transcriptome_fasta, thread)
        else:
            transcriptome_fasta = extract_transcriptome_from_genome(gffread, genome_fasta, annot)
            transcriptome_index = build_salmon_index(salmon, transcriptome_fasta, thread)

        # 1. custome outdir structure
        structure_list = ['fastq_trimmed', 'qc/fastqc', 'qc/multiqc', 'qc/multiqc/fastqc_report',
                          'qc/multiqc/salmon_report','salmon', 'quantification']
        trim_dir, fastqc_dir, multiqc_dir, multiqc_fastqc_report, multiqc_salmon_report, salmon_dir, quant_dir = make_outdir_structure(out_dir, structure_list)
       
        # 2. trime adaptor and filter low quality sequences
        if thread > 64:
            print('Number of threads is to large for Timmomatic, will use 64 threads')
            thread_trim = 64
        else:
            thread_trim = thread
        for sample_id in sample_ids:
            trim_adaptor(type_of_lib, thread_trim, trim_dir, sample_id, in_fq_dir, adaptor_dir)
         
        # 3. fastqc for raw and trimmed FASTQ
        qc(fastqc, fastqc_dir, thread, in_fq_dir)
        qc(fastqc, fastqc_dir, thread, trim_dir)

        # 4. multiqc for fastqc results
        mqc(multiqc, multiqc_fastqc_report, fastqc_dir)
         
        # 5. salmon quantifying in mapping-based mode, extract count and TPM, and multiqc for salmon
        for sample_id in sample_ids:
            quant_sf = salmon_quant(salmon, transcriptome_index, type_of_lib, trim_dir, sample_id, salmon_dir, thread)
            cmd_format_salmon_count = r'cat %s | cut -f 1,5 | sed "1 s/^.*$/ensembl_id\t%s/" > %s.count' % (quant_sf,sample_id, quant_sf)
            cmd_format_salmon_tpm = r'cat %s | cut -f 1,4 | sed "1 s/^.*$/ensembl_id\t%s/" > %s.tpm' % (quant_sf,sample_id, quant_sf)
            subprocess.run(cmd_format_salmon_count, shell=True, check=True)
            subprocess.run(cmd_format_salmon_tpm, shell=True, check=True)
        mqc(multiqc, multiqc_salmon_report, salmon_dir)
        
        # 6. merge count, TPM (transcript) 
        cmd_merge_count = r"""paste `find %s -name quant.sf.count` | awk 'BEGIN{FS=OFS="\t"} {line=$1; for(i=2;i<=NF;i+=2) line=line"\t"$i; print line}' > %s""" % (salmon_dir, join_path(salmon_dir,'count.merged.tsv'))
        cmd_merge_tpm = r"""paste `find %s -name quant.sf.tpm` | awk 'BEGIN{FS=OFS="\t"} {line=$1; for(i=2;i<=NF;i+=2) line=line"\t"$i; print line}' > %s""" % (salmon_dir, join_path(salmon_dir,'tpm.merged.tsv'))
        subprocess.run(cmd_merge_count, shell=True, check=True)
        subprocess.run(cmd_merge_tpm, shell=True, check=True)


    elif args.command == 'gMode':
        # arguments
        (genome_index, genome_fasta, annot, in_fq_dir, out_dir, type_of_lib, adaptor_dir, 
         thread, read_length, trimmmomatic, fastqc, multiqc, STAR, samtools, Rscript
        ) = (args.genome_index, args.genome_fasta, args.annot, args.in_fq_dir, args.out_dir, args.type_of_lib, args.adaptor_dir,
             args.thread, args.read_length, args.trimmomatic, args.fastqc, args.multiqc, args.STAR, args.samtools, args.rscript
            )
        if type_of_lib == 'SE':
            sample_ids = [f.rstrip('.fastq.gz') for f in os.listdir(in_fq_dir) if f.endswith('.fastq.gz')]
        else:
            sample_ids= [f.rstrip('_1.fastq.gz') for f in os.listdir(in_fq_dir) if f.endswith('_1.fastq.gz')]
            
        # check if -g or -G at least one exist
        if genome_index == None and genome_fasta == None:
            os.sys.exit('ERROR: -g or -G must be set.  Please set one')

        # 0. build STAR index
        if genome_fasta is not None:
            print("***Using reference genome fasta file to build STAR genome index before aligning...***")
            genome_index = build_star_index(genome_fasta, annot, thread, read_length)

        # 1. custome outdir structure
        structure_list = ['fastq_trimmed', 'qc/fastqc', 'qc/multiqc', 'qc/multiqc/fastqc_report',
                          'qc/multiqc/star_report','alignment/star', 'quantification']
        trim_dir, fastqc_dir, multiqc_dir, multiqc_fastqc_report, multiqc_star_report, align_dir, quant_dir = make_outdir_structure(out_dir, structure_list)
        
        # 2. trime adaptor and filter low quality sequences
        if thread > 64:
            print('Number of threads is to large for Timmomatic, will use 64 threads')
            thread_trim = 64
        else:
            thread_trim = thread
        for sample_id in sample_ids:
            trim_adaptor(type_of_lib, thread_trim, trim_dir, sample_id, in_fq_dir, adaptor_dir)
         
        # 3. fastqc for raw and trimmed FASTQ
        qc(fastqc, fastqc_dir, thread, in_fq_dir)
        qc(fastqc, fastqc_dir, thread, trim_dir)

        # 4. multiqc for fastqc results
        mqc(multiqc, multiqc_fastqc_report, fastqc_dir)
         
        # 5. align reads to genome
        for sample_id in sample_ids:
            align(STAR, sample_id, type_of_lib, thread, genome_index, trim_dir, align_dir)
            sample_prefix = join_path(align_dir,sample_id,sample_id)
            cmd_format_star_count = r'tail -n +5 {0}.ReadsPerGene.out.tab | sed "1 i\ensembl_id\t{1}\t${1}_sense\t${1}_antisense" - > {0}.ReadsPerGene.out.formatted.tsv'.format(sample_prefix, sample_id)
            subprocess.run(cmd_format_star_count, shell=True, check=True)
        mqc(multiqc, multiqc_star_report, align_dir)
        
        # 6. sort and index BAM
        for sample_id in sample_ids:
            sort_bam(samtools, thread, align_dir, sample_id)
 
        # 7. format, merge count
        merge_count(align_dir, quant_dir)
            
        # 8. count to TPM while adding gene annotation
        count2TPM(Rscript, annot, quant_dir)

    # ---------------------- 
    print('OK!  The whole pipeline done!')
    elasped_time = datetime.now() - START_TIME
    os.sys.stderr.write("** Pipeline run time: %d seconds **\n" % (elasped_time.seconds))


if __name__ == '__main__':
    main()




 
