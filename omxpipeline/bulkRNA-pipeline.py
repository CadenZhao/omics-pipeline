def extract_transcriptome_from_genome(gffread, genome_fasta, annot):
    transcriptome_file = genome_fasta.rstrip('fa')+'transcript.fa'
    cmd = r'%s %s -g %s -w %s' % (gffread, annot, genome_fasta, transcriptome_file)
    subprocess.run(cmd, shell=True, check=True)
    return transcriptome_file


def build_salmon_index(salmon, transcriptome_fasta, thread):
    salmon_index_dir  = transcriptome_fasta.rstrip('fa')+'salmon.index'
    mkdir(salmon_index_dir)
    cmd = r'%s index -t %s -i %s -p %d' % (salmon, transcriptome_fasta, salmon_index_dir, thread)
    subprocess.run(cmd, shell=True, check=True)
    return salmon_index_dir


def salmon_quant(salmon, salmon_index_dir, type_of_lib, trim_dir, sample_id, salmon_out_dir, thread):
    sample_dir = join_path(salmon_out_dir, sample_id)
    cmd_mkdir_for_sample = r'mkdir -p %s' % sample_dir
    if type_of_lib == 'SE':
        read_file_param = '-r %s.trimmed.fastq.gz' % join_path(trim_dir,sample_id)
    elif type_of_lib == 'SE':
        read_file_param = '-1 {0}_1.trimmed.fastq.gz -2 {0}_2.trimmed.fastq.gz'.format(join_path(trim_dir,sample_id))
    cmd_quant = r'%s quant -i %s -l A %s -o %s --gcBias -p %d' % (salmon, salmon_index_dir, read_file_param, sample_dir, thread)

    subprocess.run(cmd_mkdir_for_sample, shell=True, check=True)
    subprocess.run(cmd_quant, shell=True, check=True)
    return join_path(sample_dir, 'quant.sf')


