    # 2. genome-based mode
    gmode_command = commands.add_parser('gMode', help='Perform genome-based bulkRNA-seq pipelin', description='Perform genome-based bulkRNA-seq pipeline. (Reconstructed transcriptome will be used)')

    gmode_group = gmode_command.add_mutually_exclusive_group()
    gmode_group.add_argument('-g', '--genome_index', metavar='<STAR index>', type=str, required=False, help='genome star index directory. It is generated by STAR --runMode genomeGenerate')
    gmode_group.add_argument('-G', '--genome_fasta', metavar='<genome.fa>', type=str, required=False, help='genome fasta file. It will be ignored if -g be assigned and STAR index is exting')

    gmode_command.add_argument('-a', '--annot', metavar='<ref.gtf>', type=str, required=True, help='annotation GTF file. It should not be a compressed file')
